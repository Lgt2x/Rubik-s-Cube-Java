public class Face{
	int adjacent1;
	int adjacent2;
	int adjacent3;
	int adjacent4;
	int valeur;

	public Face(int adjacent1, int adjacent2, int adjacent3, int adjacent4, int valeur) {
		this.adjacent1 = adjacent1;
		this.adjacent2 = adjacent2;
		this.adjacent3 = adjacent3;
		this.adjacent4 = adjacent4;
		this.valeur = valeur;
	}
}
